package com.sunsett.debt_reminder.documentation;

/*  ========================================
 *  | Estructura del proyecto DebtReminder |
 *  ========================================
 *
 *      Paquete com.sunsett.debt_reminder
 *          Contiene la estructura base del proyecto.
 *
 *      Paquete com.sunsett.debt_reminder.config
 *          Contiene clases de configuración para la aplicación.
 *
 *      Paquete com.sunsett.debt_reminder.controllers
 *          Contiene los controladores REST que manejan las solicitudes HTTP entrantes.
 *
 *      Paquete com.sunsett.debt_reminder.converters
 *          Contiene clases que se encargan de la conversión entre diferentes tipos de datos.
 *
 *      Paquete com.sunsett.debt_reminder.exceptions
 *          Contiene clases para manejar excepciones personalizadas.
 *
 *      Paquete com.sunsett.debt_reminder.model
 *          Contiene las clases del modelo de datos.
 *          Subpaquetes:
 *              dto: Contiene clases de transferencia de datos (Reportes).
 *              entities: Contiene entidades que representarán las tablas en la base de datos.
 *
 *      Paquete com.sunsett.debt_reminder.repository
 *          Contiene interfaces de repositorio para la interacción con la base de datos.
 *
 *      Paquete com.sunsett.debt_reminder.services
 *          Contiene servicios que implementan la lógica de negocio de la aplicación.
 *          Minimo por cada entidad, tiene que existir un service.
 *
 *
 *  =======================
 *  | Comandos con Github |
 *  =======================
 *
 *      Ver todas las ramas:
 *          git branch -a
 *
 *      Cambiar de rama:
 *          git checkout (RAMA)
 *
 *      Verificar rama en la que estoy:
 *          git Branch
 *
 *      Crear rama en local:
 *          git checkout -b (RAMA)
 *
 *      Crear un commit y seleccionar rama a pushear:
 *          git add .
 *          git commit -m "(COMENTARIO)"
 *          git push origin (RAMA)
 *
 */
